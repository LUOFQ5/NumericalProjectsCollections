float fresnelConductor(float cosT, float eta, float kappa) {
    float cosT2  = cosT * cosT;
    float sinT2  = 1.0f - cosT2;
    float eta2   = eta * eta;
    float kappa2 = kappa * kappa;

    float t0 = eta2 - kappa2 - sinT2;
    float a2plusb2 = sqrtf(t0 * t0 + 4.0f * eta2 * kappa2);
    float t1 = a2plusb2 + cosT2;
    float temp = 0.5f * (a2plusb2 + t0);
    if( temp > 0.0 ) {
        float a  = sqrtf(temp);
        float t2 = 2.0f * a * cosT;
        float Rs = (t1 - t2) / (t1 + t2);

        float t3 = cosT2 * a2plusb2 + sinT2 * sinT2;
        float t4 = t2 * sinT2;   
        float Rp = Rs * (t3 - t4) / (t3 + t4);

        return 0.5f * (Rs + Rp);
    } else {
        return 1.0f;
    }
}

inline
void evaluateFresnelCurves(float cT, float* values) {
    uint32_t index = (uint32_t)(cT*NB_POINTS);
    index = std::min<uint32_t>(index, NB_POINTS-1);
    values[0] = fresnel_curves[index][0];
    values[1] = fresnel_curves[index][1];
    values[2] = fresnel_curves[index][2];
    values[3] = fresnel_curves[index][3];
}

inline
void fresnelToCoeffs(float eta, float kappa, float* coeffs) {

    coeffs[0] = fresnelConductor(1.0, eta, kappa);
    coeffs[1] = (coeffs[0] >= 0.0f) ? 1.0f - coeffs[0] : 0.0f;

    const float ct1 = 0.12012012012012012;
    const float ct2 = 0.4344344344344344;
    const unsigned int i1 = 120;
    const unsigned int i2 = 434;

    const float dF1 = fresnelConductor(ct1, eta, kappa) - (coeffs[0] + coeffs[1]*fresnel_curves[i1][1]);
    const float dF2 = fresnelConductor(ct2, eta, kappa) - (coeffs[0] + coeffs[1]*fresnel_curves[i2][1]);
    
    const float A11 =  fresnel_curves[i2][3];
    const float A12 = -fresnel_curves[i2][2];
    const float A21 = -fresnel_curves[i1][3];
    const float A22 =  fresnel_curves[i1][2];
    const float det = A11*A22 - A12*A21;
    
    coeffs[2] = (A11*dF1 + A21*dF2) / det;
    coeffs[3] = (A12*dF1 + A22*dF2) / det;
}
