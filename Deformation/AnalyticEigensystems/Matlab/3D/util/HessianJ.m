function [H] = HessianJ(F)
f0 = F(:,1);
f1 = F(:,2);
f2 = F(:,3);

F0 = [     0 -f0(3)  f0(2);
       f0(3)      0 -f0(1);
      -f0(2)  f0(1)      0];
      
F1 = [     0 -f1(3)  f1(2);
       f1(3)      0 -f1(1);
      -f1(2)  f1(1)      0];
      
F2 = [     0 -f2(3)  f2(2);
       f2(3)      0 -f2(1);
      -f2(2)  f2(1)      0];
      
Z = zeros(3,3);
H = [  Z -F2  F1;
      F2   Z -F0;
     -F1  F0   Z];
end
